// Функция для генерации временных меток
function generateTimes(startHour: number, startMin: number, count: number): string[] {
  const times: string[] = [];
  let hour = startHour;
  let min = startMin;
  
  for (let i = 0; i < count; i++) {
    times.push(`${hour.toString().padStart(2, '0')}:${min.toString().padStart(2, '0')}`);
    min += 5;
    if (min >= 60) {
      min = 0;
      hour++;
      if (hour >= 24) hour = 0;
    }
  }
  
  return times;
}

